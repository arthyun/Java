public class AgeOfGildong {
    public static void main(String[] args) {
        int gildongAge = 8;
        int ageAfter10 = gildongAge + 10;
        int ageAfter20 = gildongAge + 20;
        int ageAfter30 = gildongAge + 30;

        System.out.println("현재 나이: " + gildongAge + "세");
        System.out.println("10년 후 나이: " + ageAfter10 + "세");
        System.out.println("20년 후 나이: " + ageAfter20 + "세");
        System.out.println("30년 후 나이: " + ageAfter30 + "세");
    }
}
